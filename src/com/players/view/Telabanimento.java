    private void PreencheCB() {

        GmDAO vdao = new GmDAO();

        for (Gm v : vdao.FindAll()) {

            cmbGM.addItem(v);
        }

        JogadoresDAO cdao = new JogadoresDAO();

        for (Jogadores c : cdao.FindAll()) {

            cmbPlayer.addItem(c);
        }
    }

    private void PreencheCBTable(Gm idban, Jogadores idJogadores) {
        cmbPlayer.setEditable(true);
        cmbGM.setEditable(true);
        cmbPlayer.setSelectedItem(idban);
        cmbGM.setSelectedItem(idJogadores);
        cmbPlayer.setEditable(false);
        cmbGM.setEditable(false);
        txtNome.setText(idJogadores.getNome());
        txtNick.setText(idJogadores.getNick());
        txtCadastro.setText(idJogadores.getCadastro());
        txtCampeaoFav.setText(String.valueOf(idJogadores.getChampionfav()));
        txtIDPlayer.setText(String.valueOf(idJogadores.getidjogador()));
        txtSobrenome.setText(idban.getSobrenome());
        txtIDGM.setText(String.valueOf(idban.getidgm()));
    }

