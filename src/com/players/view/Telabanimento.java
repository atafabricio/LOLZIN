    private void realizaban() {
        Jogadores player = new Jogadores();
        player.setidjogador(Integer.parseInt(txtIDPlayer.getText()));
        Gm gm = new Gm();
        gm.setidgm(Integer.parseInt(txtIDGM.getText()));

        BanimentoDAO dao = new BanimentoDAO();
        Banimento ban = new Banimento();

        ban.setJogador(player);
        ban.setGm(gm);

        dao.save(ban);
    }

    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTableVendas.getModel();
        modelo.setNumRows(0);
        BanimentoDAO vdao = new BanimentoDAO();
        for (Banimento v : vdao.FindAll()) {
            modelo.addRow(new Object[]{
                v.getIdban(),
                v.getGm(),
                v.getJogador()
            });

        }
    }

